Autogyro
ELA cougar.jpg
ELA 07S in flight.
An autogyro (from Greek αὐτός + γύρος, self-turning), also known as gyroplane, gyrocopter, or rotaplane, is a type of rotorcraft that uses an unpowered rotor in autorotation to develop lift, and an engine-powered propeller, similar to that of a fixed-wing aircraft, to provide thrust. While similar to a helicopter rotor in appearance, the autogyro's rotor must have air flowing through the rotor disc to generate rotation. Invented by the Spanish engineer Juan de la Cierva to create an aircraft that could fly safely at low speeds, the autogyro was first flown on January 9, 1923, at Cuatro Vientos Airfield in Madrid.[1] De la Cierva's aircraft resembled the fixed-wing aircraft of the day, with a front-mounted engine and propeller in a tractor configuration to pull the aircraft through the air.

Under license from Cierva in the 1920s and 1930s, the Pitcairn & Kellett companies made further innovations.[2] Late-model autogyros patterned after Etienne Dormoy's Buhl A-1 Autogyro and Igor Bensen's designs feature a rear-mounted engine and propeller in a pusher configuration. The term Autogiro was a trademark of the Cierva Autogiro Company, and the term Gyrocopter was used by E. Burke Wilford who developed the Reiseler Kreiser feathering rotor equipped gyroplane in the first half of the twentieth century. The latter term was later adopted as a trademark by Bensen Aircraft.</p>      
  
